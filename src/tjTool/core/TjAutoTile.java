    public static TextureRegion[] atlasRegions(String name) {
        return atlasRegions(name, 32, 32);
    }

    public static TextureRegion[] atlasRegions(String name, int tileWidth, int tileHeight) {
        TextureRegion region = Core.atlas.find(name + "-atlas");

        int x = region.getX();
        int y = region.getY();
        int width = region.width;
        int height = region.height;
        int widthTo = width + x;
        int heightTo = height + y;

        TextureRegion[] regions = new TextureRegion[(width / tileWidth + 1) * (height / tileHeight + 1)];
        int idx = 0;

        for(int arrY = y; arrY < heightTo; arrY += tileHeight)
            for(int arrX = x; arrX < widthTo; arrX += tileWidth)
                regions[idx++] = new TextureRegion(region.texture, arrX, arrY, tileWidth, tileHeight);

        return regions;
    }

